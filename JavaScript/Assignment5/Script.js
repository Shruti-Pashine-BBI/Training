// Arithmetic

// Addition
const now = 3000;
const add = 2000 + 1945;
const add2 = 30 + 10;
console.log(add, add2);
console.log(add + add2);
console.log(add + add2 + now);

//concatenation
const firstname = "Jonas";
const LastName = "Brahim";
console.log(firstname + LastName);

//Subtraction
const a = 2000 - 1945;
const b = 30 - 10;

console.log(a, b);

console.log(firstname, "->", add2);

//Assignment Operator

let x = 10 + 4;
console.log(x);
x += 5;
console.log(x);

//Comparision Operator
console.log(add2 < add);
console.log(add <= now);

const fullage = add <= 18;
console.log(fullage);

console.log(now - 1991 > now - 2010);
